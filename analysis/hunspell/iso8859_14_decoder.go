package hunspell

// ISO8859_14Table maps bytes in the range [0xA0, 0xFF] to their ISO-8859-14
// (Latin-8, Celtic) Unicode code points. Many Hunspell dictionaries use this
// encoding but the Go standard library (like Java) does not include it
// natively.
//
// This is the Go port of
// org.apache.lucene.analysis.hunspell.ISO8859_14Decoder from Apache Lucene
// 10.4.0.
var ISO8859_14Table = [96]rune{
	0x00A0, 0x1E02, 0x1E03, 0x00A3, 0x010A, 0x010B, 0x1E0A, 0x00A7,
	0x1E80, 0x00A9, 0x1E82, 0x1E0B, 0x1EF2, 0x00AD, 0x00AE, 0x0178,
	0x1E1E, 0x1E1F, 0x0120, 0x0121, 0x1E40, 0x1E41, 0x00B6, 0x1E56,
	0x1E81, 0x1E57, 0x1E83, 0x1E60, 0x1EF3, 0x1E84, 0x1E85, 0x1E61,
	0x00C0, 0x00C1, 0x00C2, 0x00C3, 0x00C4, 0x00C5, 0x00C6, 0x00C7,
	0x00C8, 0x00C9, 0x00CA, 0x00CB, 0x00CC, 0x00CD, 0x00CE, 0x00CF,
	0x0174, 0x00D1, 0x00D2, 0x00D3, 0x00D4, 0x00D5, 0x00D6, 0x1E6A,
	0x00D8, 0x00D9, 0x00DA, 0x00DB, 0x00DC, 0x00DD, 0x0176, 0x00DF,
	0x00E0, 0x00E1, 0x00E2, 0x00E3, 0x00E4, 0x00E5, 0x00E6, 0x00E7,
	0x00E8, 0x00E9, 0x00EA, 0x00EB, 0x00EC, 0x00ED, 0x00EE, 0x00EF,
	0x0175, 0x00F1, 0x00F2, 0x00F3, 0x00F4, 0x00F5, 0x00F6, 0x1E6B,
	0x00F8, 0x00F9, 0x00FA, 0x00FB, 0x00FC, 0x00FD, 0x0177, 0x00FF,
}

// DecodeISO8859_14 decodes a byte slice encoded in ISO-8859-14 to a UTF-8
// string. Bytes below 0xA0 are mapped 1:1 (identical to ASCII/Latin-1).
func DecodeISO8859_14(b []byte) string {
	runes := make([]rune, len(b))
	for i, ch := range b {
		if ch >= 0xA0 {
			runes[i] = ISO8859_14Table[ch-0xA0]
		} else {
			runes[i] = rune(ch)
		}
	}
	return string(runes)
}
